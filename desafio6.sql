SELECT
(SELECT CONCAT(e.FIRST_NAME, ' ', e.LAST_NAME) FROM hr.employees AS e WHERE e.EMPLOYEE_ID = h.EMPLOYEE_ID) AS 'Nome completo',
(SELECT JOB_TITLE FROM hr.jobs AS j WHERE j.JOB_ID = h.JOB_ID) AS Cargo,
h.START_DATE AS 'Data de in√≠cio do cargo',
(SELECT DEPARTMENT_NAME FROM hr.departments AS d WHERE d.DEPARTMENT_ID = h.DEPARTMENT_ID) AS Departamento
FROM hr.job_history AS h
ORDER BY (SELECT CONCAT(e.FIRST_NAME, ' ', e.LAST_NAME) FROM hr.employees AS e WHERE e.EMPLOYEE_ID = h.EMPLOYEE_ID) DESC, Cargo ASC;
